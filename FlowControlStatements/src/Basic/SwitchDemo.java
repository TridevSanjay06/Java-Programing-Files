package Basic;

public class SwitchDemo {
	public static void main(String [] args) {
		
		int x = 5;
		
		switch (x) {
		
		case 1: 
			System.out.println("case 1");
			break;
		
		case 2:
			System.out.println("case 2");
			break;
			
		default:
			System.out.println("default");
			
		}
	}

}
